package code;

public class NodeUpdated {
Node node ;
String S;
public NodeUpdated(Node node, String s) {
	super();
	this.node = node;
	S = s;
}
public Node getNode() {
	return node;
}
public void setNode(Node node) {
	this.node = node;
}
public String getS() {
	return S;
}
public void setS(String s) {
	S = s;
}
}
